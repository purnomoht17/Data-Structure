/*
* learning how to variable
*
 */
public class Variable {
    public static void main(String[] args) {
        int bulat;
        bulat = 2024;
        double phi;
        phi = 3.14;
        float desimal;
        desimal = 1.333f;
        char huruf;
        huruf = 'a';
        char uniChar;
        uniChar = '\u0061';
        char emoji ;
        emoji = '\u263A';
        boolean status;
        status = true;

        System.out.println("nilai dari bulat = "+ bulat);
        System.out.println("nilai dari phi = "+ phi);
        System.out.println("nilai dari desimal = "+ desimal);
        System.out.println(huruf);
        System.out.println(uniChar);
        System.out.println(emoji);
        System.out.println(status);
    }
}
